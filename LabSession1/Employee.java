public class Employee {

    String name;
    String position;
    float salary;

    static int objectCount = 0;

    /////////////////////////////////////////////////////////////////////////////
    //
    /////////////////////////////////////////////////////////////////////////////
    public Employee() {
        this("NA", "NA", 0.0f);
    }

    /////////////////////////////////////////////////////////////////////////////
    //
    /////////////////////////////////////////////////////////////////////////////
    public Employee(String name, String position, float salary) {
        this.name = name;
        this.position = position;
        this.salary = salary;

        objectCount++;
        System.out.println("[Object Created] Object count: " + getObjectCount());
    }

    /////////////////////////////////////////////////////////////////////////////
    //
    /////////////////////////////////////////////////////////////////////////////
    public static int getObjectCount() {
        return Employee.objectCount;
    }

    /////////////////////////////////////////////////////////////////////////////
    //
    /////////////////////////////////////////////////////////////////////////////
    @Override
    public String toString() {
        return name + " " + position + " " + salary;
    }

    /////////////////////////////////////////////////////////////////////////////
    // Main method to demonstrate class functionality
    /////////////////////////////////////////////////////////////////////////////
    public static void main(String[] args) {
        
        Employee e1 = new Employee("Deepak", "CEO", 2500000);
        System.out.println(e1);

        Employee e2 = new Employee("Fahed", "Analyst", 25000);
        System.out.println(e2);

        Employee e3 = new Employee("Hritik", "Photographer", 35000);
        System.out.println(e3);
    }

    /////////////////////////////////////////////////////////////////////////////
    //
    /////////////////////////////////////////////////////////////////////////////
}